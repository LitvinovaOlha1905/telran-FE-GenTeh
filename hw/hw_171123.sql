-- Задача 4. Дайте короткий ответ на вопрос: 
-- что такое проекция в БД и зачем она нужна?

-- В контексте баз данных, термин "проекция" относится к операции выбора определенных столбцов из таблицы или результатов запроса. Проекция позволяет создавать новую таблицу или результат запроса, содержащие только те столбцы, которые действительно нужны, и отбрасывать лишнюю информацию.

-- Проекция определяется как операция, которая возвращает подмножество атрибутов (столбцов) из каждой записи в таблице. В SQL, оператор SELECT выполняет функцию проекции, позволяя указывать, какие столбцы необходимо включить в результат.