-- Задача 1. Вывести товары из категорий 1 и 4 с ценой от 50 до 150 EUR

SELECT * 
FROM Products

WHERE
		Price BETWEEN 50 AND 150
		AND
    	CategoryID IN (1, 4)

-- Задача 2. Вывести товары (проекция: название, цена_со_скидкой 0.5 процента)

SELECT 
	ProductName,
   Price * .995 AS Price_down
FROM Products

